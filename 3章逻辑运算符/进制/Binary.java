package 进制;

public class Binary {
	 public static void main(String[] args) {
		 
		 
		 
		//1 先得到2的部码 => 2的源码 00000000 00000000 00000000 00000010
		//  2的补码 00000000 00000000 00000000 00000010
		//2 3的补码 => 3的源码 00000000 00000000 00000000 00000011
		//  3的补码 00000000 00000000 00000000 00000011
		//3 按位& 
		//  00000000 00000000 00000000 00000010
		//  00000000 00000000 00000000 00000011
		//  00000000 00000000 00000000 00000010  计算机运算后的补码
		//  00000000 00000000 00000000 00000010  返回源码
		//  结果就是 00000000 00000000 00000000 00000010 -> 2
		 
		 
		System.out.println(2&3);
		
//		1 先得到-2的源码 10000000 00000000 00000000 00000010
//		2 -2 反码       11111111 11111111 11111111 11111101
//		3 -2 补码 加一   11111111 11111111 11111111 11111110
//		4 ～-2操作      00000000  00000000 00000000 00000001
//		5 源码          00000000  00000000 00000000 00000001 => 1
		System.out.println(~-2);
		
//		1 2的补码       00000000 00000000 00000000 00000010
//		2 按位取反      11111111 11111111 11111111 11111101  运算后的补码
//		3 运算后的反码   11111111 11111111 11111111 11111100  
//		4 运算后的源码   10000000 00000000 00000000 00000011 => -3
		
		
		System.out.println(1 << 2);

		System.out.println(1 >> 2);
//		System.out.println();
		
		
	}
}
